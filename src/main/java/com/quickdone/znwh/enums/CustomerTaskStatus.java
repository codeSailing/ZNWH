public enum CustomerTaskStatus {
    WAIT("待执行",1),EXECUTEING("执行中",2),HANGUP("挂断",3),COMPLETE("执行完毕",4),USERBUSY("用户忙",5);
    int value;
    String name;
    private CustomerTaskStatus(String name,int value){
        this.value=value;
        this.name=name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
